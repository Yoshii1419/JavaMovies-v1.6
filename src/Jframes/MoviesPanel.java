    boolean isDouble(String str){
        
        if(this.ImdbScoreTextField.getText().compareTo("") != 0) {
            try {
                Double.parseDouble(str);
                return true;
            } catch (NumberFormatException e) {
                return false;
            }
        }
        else{
            return true; // se for espaço, deve retornar verdadeiro //
        }
    }
    
