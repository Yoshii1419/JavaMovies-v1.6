    private void ImageCoverSendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImageCoverSendButtonActionPerformed
        // TODO add your handling code here:
        String url_input;
        url_input = this.ImageCoverTextField.getText();

        Image image = null;
        URL url = null;
        try {
            url = new URL(url_input);
            image = ImageIO.read(url);
        } catch (MalformedURLException ex) {
            System.out.println("Malformed URL");
        } catch (IOException iox) {
            System.out.println("Can not load file");
        }

        ImageIcon imgIcon = new ImageIcon(image);
        // instrução abaixo realiza o ajuste da imagem para o alinhamento da JLabel
        Image img = imgIcon.getImage().getScaledInstance(this.ImageCoverLabel.getWidth(), this.ImageCoverLabel.getHeight(), Image.SCALE_SMOOTH);
        this.ImageCoverLabel.setIcon(new ImageIcon(img));

    }//GEN-LAST:event_ImageCoverSendButtonActionPerformed

    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        Titles title = new Titles();
        // recuperando valores inseridos nos campos da tela //
        title.setTitleName(this.TitleTextField.getText());
        title.setDirector(this.DirectorTextField.getText());
        title.setWriter(this.WriterTextField.getText());
        title.setImageCover(this.ImageCoverTextField.getText());
        title.setReleaseYear(Integer.parseInt(this.YearComboBox.getSelectedItem().toString()));
        
        // Validando campo IMDB_SCORE //
        // se dado NÃO for double //
        if(!isDouble(this.ImdbScoreTextField.getText())){ 
            System.out.println(isDouble(this.ImdbScoreTextField.getText()));
            this.MsgErroLabel.setText("Campo IMDB com formato inválido!");
        }
        else{
            this.MsgErroLabel.setText("");
        }
        // precisa ser posto aqui para que barre o salvamento no banco //
        if(this.ImdbScoreTextField.getText().compareTo("") != 0){
            title.setImdbScore(Double.parseDouble(this.ImdbScoreTextField.getText()));
        }
        
        // verificando se o valor incluído está entre 0 e 10 //
        // Caso não esteja:
        if(title.getImdbScore() < 0.0 || title.getImdbScore() > 10.0) {
            this.MsgErroLabel.setText("Nota IMDB deve ser entre 0 e 10!");
            Double.parseDouble(""); // força erro, impedindo inserção do campo no banco //
        }
        
        // verificando valores de Gênero //
        // se item for o primeiro do combo, setar como nulo para o Banco //
        if(this.GenreComboBox.getSelectedItem().toString().compareTo("--") == 0) {
            title.setGenre(null);
        }
        else {
            title.setGenre(this.GenreComboBox.getSelectedItem().toString());
        }
        
        DataBaseManager dataBase = new DataBaseManager();
        // abrindo conexão com o banco de dados //
        dataBase.OpenConnectionDataBase();
        
        // verificando se Título já existe na base de dados //
        // se existir, retorna true para a condição //
        if(!dataBase.VerifyIfExistTitle(title.getTitleName(), title.getDirector())){
            // se nao existir, executar querys //
            dataBase.ExecuteQueryInsert(title);
            // caixa de diálogo //
            JOptionPane.showMessageDialog(null, "Your Title was Included!");
            InitMoviesPanel(); // limpa tela de inclusão após registro ser salvo //
        }
        else {
            this.MsgErroLabel.setText("Title Already Exist for this Director!");
        }
        
        dataBase.CloseConnection();
    }//GEN-LAST:event_SaveButtonActionPerformed


